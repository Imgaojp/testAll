public class Largest {

    /**
     * 传入整形数组，返回最大值
     * @param list （整形数组）
     * @return 最大值
     */
    public static int largest(int[] list) {
        int max = Integer.MIN_VALUE;
        if (list.length == 0) {
            throw new RuntimeException("Empty list");
        }
        for (int i = 0; i <= list.length-1; i++) {
            if (list[i] > max) {
                max = list[i];
            }
        }
        return max;
    }
}
